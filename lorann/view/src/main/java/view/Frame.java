
import javax.swing.JFrame;

public class Frame extends JFrame{
	
	private static final long serialversionVID = 1L;
	
	public Frame(string title)	{
		setTitle(title);
		setsize(700, 550);
		setDefaultCloseOperation(Jframe.Exit_ON_CLOSE);
		setResizable(True);
		setLocationRelativeTo(null);
		setVisible(true);
	}

}
